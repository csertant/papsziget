export type TranslationKey = keyof typeof translations;

export function useTranslation(language: LanguageState) {
    const t = (key: TranslationKey): string => {
        return translations[key]?.[language.current] || translations[key]?.["hu"] || key;
    };
    return { t, currentLang: language.current };
}